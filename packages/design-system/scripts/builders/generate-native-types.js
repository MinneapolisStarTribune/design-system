const fs = require('fs');
const path = require('path');

const THEMES_DIR = 'dist/mobile/themes';
const TYPOGRAPHY_DIR = 'dist/mobile/typography';
const TYPES_DIR = 'src/types';

/**
 * Theme files to generate types for.
 * Each entry: [module alias, file name, interface name]
 */
const THEME_FILES = [
  ['@mobile/themes/startribune-light.js', 'startribune-light.js', 'StartribuneLightTheme'],
  ['@mobile/themes/startribune-dark.js', 'startribune-dark.js', 'StartribuneDarkTheme'],
  ['@mobile/themes/varsity-light.js', 'varsity-light.js', 'VarsityLightTheme'],
  ['@mobile/themes/varsity-dark.js', 'varsity-dark.js', 'VarsityDarkTheme'],
];

/**
 * Typography files to generate types for.
 * Each entry: [module alias, file name, interface name]
 */
const TYPOGRAPHY_FILES = [
  [
    '@mobile/typography/startribune-typography.js',
    'startribune-typography.js',
    'StartribuneTypography',
  ],
  ['@mobile/typography/varsity-typography.js', 'varsity-typography.js', 'VarsityTypography'],
];

/**
 * Map a runtime value to its TypeScript type string.
 */
function tsType(value) {
  if (typeof value === 'string') return 'string';
  if (typeof value === 'number') return 'number';
  if (typeof value === 'boolean') return 'boolean';
  if (typeof value === 'object' && value !== null) return 'TextStyle';
  return 'unknown';
}

/**
 * Load a CommonJS module from dist, clearing the require cache first
 * so we always get the freshly-built version.
 */
function loadFresh(filePath) {
  const resolved = require.resolve(filePath);
  delete require.cache[resolved];
  return require(resolved);
}

/**
 * Generate typed interface properties from a token object.
 * Returns an array of "  propertyName: type;" lines, sorted alphabetically.
 */
function generateProperties(tokens, defaultType) {
  return Object.entries(tokens)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([key, value]) => `  ${key}: ${defaultType || tsType(value)};`);
}

/**
 * Generate src/types/mobile-themes.d.ts with exact property names and types
 * for each brand/mode theme file.
 */
function generateThemeTypes() {
  const interfaces = [];
  const moduleDecls = [];
  const interfaceNames = [];

  for (const [moduleAlias, fileName, interfaceName] of THEME_FILES) {
    const filePath = path.join(process.cwd(), THEMES_DIR, fileName);
    if (!fs.existsSync(filePath)) {
      console.warn(`⚠️  Theme file not found: ${filePath}, skipping`);
      continue;
    }

    const tokens = loadFresh(filePath);
    const props = generateProperties(tokens);

    // No 'export' keyword — keeps this file as a global ambient declaration file
    // so that 'declare module' blocks are automatically available to all files.
    // Theme interfaces only use string | number, so no imports are needed.
    interfaces.push(`interface ${interfaceName} {\n${props.join('\n')}\n}`);
    moduleDecls.push(
      [
        `declare module '${moduleAlias}' {`,
        `  const theme: ${interfaceName};`,
        `  export = theme;`,
        `}`,
      ].join('\n'),
    );
    interfaceNames.push(interfaceName);
  }

  const content = [
    `/**`,
    ` * Auto-generated by \`yarn tokens\`. Do not edit manually.`,
    ` * Re-run \`yarn tokens\` to regenerate after token changes.`,
    ` *`,
    ` * Internal use only — provides type-checking and autocomplete for developers`,
    ` * authoring native components in this repo. This file is NOT published to npm`,
    ` * and is never seen by consuming apps.`,
    ` *`,
    ` * This file has NO top-level import/export so it remains a global ambient`,
    ` * declaration file. The 'declare module' blocks are automatically available`,
    ` * to all TypeScript files without explicit import.`,
    ` */`,
    ``,
    ...interfaces.flatMap((iface) => [iface, ``]),
    ...moduleDecls.flatMap((decl) => [decl, ``]),
  ].join('\n');

  const outPath = path.join(process.cwd(), TYPES_DIR, 'mobile-themes.d.ts');
  fs.writeFileSync(outPath, content, 'utf8');
  console.log(
    `✓ Theme types generated → ${TYPES_DIR}/mobile-themes.d.ts (${interfaceNames.length} modules)`,
  );
}

/**
 * Generate src/types/mobile-typography.d.ts with exact property names
 * (all typed as TextStyle) for each brand typography file.
 */
function generateTypographyTypes() {
  const interfaces = [];
  const moduleDecls = [];
  const interfaceNames = [];

  for (const [moduleAlias, fileName, interfaceName] of TYPOGRAPHY_FILES) {
    const filePath = path.join(process.cwd(), TYPOGRAPHY_DIR, fileName);
    if (!fs.existsSync(filePath)) {
      console.warn(`⚠️  Typography file not found: ${filePath}, skipping`);
      continue;
    }

    const tokens = loadFresh(filePath);
    const props = generateProperties(tokens, 'TextStyle');

    // No 'export' keyword — keeps this file as a global ambient declaration file
    // so that 'declare module' blocks are automatically available to all files.
    interfaces.push(`interface ${interfaceName} {\n${props.join('\n')}\n}`);
    moduleDecls.push(
      [
        `declare module '${moduleAlias}' {`,
        `  const typography: ${interfaceName};`,
        `  export = typography;`,
        `}`,
      ].join('\n'),
    );
    interfaceNames.push(interfaceName);
  }

  const content = [
    `/**`,
    ` * Auto-generated by \`yarn tokens\`. Do not edit manually.`,
    ` * Re-run \`yarn tokens\` to regenerate after token changes.`,
    ` *`,
    ` * Internal use only — provides type-checking and autocomplete for developers`,
    ` * authoring native components in this repo. This file is NOT published to npm`,
    ` * and is never seen by consuming apps.`,
    ` *`,
    ` * This file has NO top-level import/export so it remains a global ambient`,
    ` * declaration file. The 'declare module' blocks are automatically available`,
    ` * to all TypeScript files without explicit import.`,
    ` */`,
    `type TextStyle = import('react-native').TextStyle;`,
    ``,
    ...interfaces.flatMap((iface) => [iface, ``]),
    ...moduleDecls.flatMap((decl) => [decl, ``]),
  ].join('\n');

  const outPath = path.join(process.cwd(), TYPES_DIR, 'mobile-typography.d.ts');
  fs.writeFileSync(outPath, content, 'utf8');
  console.log(
    `✓ Typography types generated → ${TYPES_DIR}/mobile-typography.d.ts (${interfaceNames.length} modules)`,
  );
}

/**
 * Generate TypeScript type declarations for all native token files.
 *
 * Custom builder required because the type declarations need exact property
 * names extracted from the generated JS files at build time. Style Dictionary
 * does not have a built-in format for generating TypeScript declaration files
 * that declare ambient modules with exact property signatures.
 *
 * Reads the built JS token files from dist/mobile/, extracts property names
 * and types, and writes .d.ts files to src/types/ with:
 * - Named interfaces with exact property names (for autocomplete)
 * - declare module blocks for the @mobile/* aliases (for import resolution)
 * - Exported union types for use in hooks
 */
async function generateNativeTypes() {
  console.log('\nGenerating native type declarations...');
  generateThemeTypes();
  generateTypographyTypes();
}

module.exports = generateNativeTypes;
