/** Maps each theme interface to its brand typography interface. */
const NATIVE_THEME_VARIANTS = [
  ['StartribuneLightTheme', 'StartribuneTypography'],
  ['StartribuneDarkTheme', 'StartribuneTypography'],
  ['VarsityLightTheme', 'VarsityTypography'],
  ['VarsityDarkTheme', 'VarsityTypography'],
];

/**
 * Generate src/types/native-theme.generated.ts — exported NativeTheme for published native entry.
 * @param {TokenInterfaceSpec[]} themeSpecs
 * @param {TokenInterfaceSpec[]} typographySpecs
 */
function generateNativeThemeType(themeSpecs, typographySpecs) {
  const exportedInterfaces = [...themeSpecs, ...typographySpecs].map(
    ({ interfaceName, props }) => `export interface ${interfaceName} {\n${props.join('\n')}\n}`,
  );

  const nativeThemeUnion = NATIVE_THEME_VARIANTS.map(
    ([themeName, typographyName]) => `  | (${themeName} & ${typographyName})`,
  ).join('\n');

  const content = [
    '/**',
    ' * Auto-generated by `yarn tokens`. Do not edit manually.',
    ' * Re-run `yarn tokens` to regenerate after token changes.',
    ' *',
    ' * Published with `@minneapolisstartribune/design-system/native` so consuming apps',
    ' * (e.g. startribune-mobile-app) get `NativeTheme` autocomplete for color, spacing,',
    ' * and typography tokens in `useNativeStyles` callbacks.',
    ' */',
    "import type { TextStyle } from 'react-native';",
    '',
    ...exportedInterfaces.flatMap((iface) => [iface, '']),
    '/** Merged base + typography tokens for the active brand and color scheme. */',
    'export type NativeTheme =',
    nativeThemeUnion,
    '',
  ].join('\n');

  const outPath = path.join(process.cwd(), TYPES_DIR, 'native-theme.generated.ts');
  fs.writeFileSync(outPath, content, 'utf8');
  console.log(`✓ NativeTheme type generated → ${TYPES_DIR}/native-theme.generated.ts`);
}

/**
 * Emit consumer-facing .d.ts beside dist/mobile token JS files (relative imports in published hooks).
 * @param {TokenInterfaceSpec[]} specs
 * @param {string} distDir
 */
function generateDistTokenDeclarations(specs, distDir) {
  for (const { fileName, interfaceName, props } of specs) {
    const outPath = path.join(process.cwd(), distDir, fileName.replace(/\.js$/, '.d.ts'));
    const content = [
      '/** Auto-generated by `yarn tokens`. Do not edit manually. */',
      "import type { TextStyle } from 'react-native';",
      `export interface ${interfaceName} {\n${props.join('\n')}\n}`,
      'declare const tokens: ' + interfaceName + ';',
      'export default tokens;',
      '',
    ].join('\n');
    fs.writeFileSync(outPath, content, 'utf8');
  }

  console.log(`✓ Dist token declarations generated → ${distDir} (${specs.length} files)`);
}

/**
 * Generate TypeScript type declarations for all native token files.
 *
 * Custom builder required because the type declarations need exact property
 * names extracted from the generated JS files at build time. Style Dictionary
 * does not have a built-in format for generating TypeScript declaration files
 * that declare ambient modules with exact property signatures.
 *
 * Reads the built JS token files from dist/mobile/, extracts property names
 * and types, and writes .d.ts files to src/types/ with:
 * - Named interfaces with exact property names (for autocomplete)
 * - declare module blocks for the @mobile/* aliases (for import resolution)
 * - Exported union types for use in hooks
 */
async function generateNativeTypes() {
  console.log('\nGenerating native type declarations...');
  const themeSpecs = generateThemeTypes();
  const typographySpecs = generateTypographyTypes();
  generateNativeThemeType(themeSpecs, typographySpecs);
  generateDistTokenDeclarations(themeSpecs, THEMES_DIR);
  generateDistTokenDeclarations(typographySpecs, TYPOGRAPHY_DIR);
}

module.exports = generateNativeTypes;
